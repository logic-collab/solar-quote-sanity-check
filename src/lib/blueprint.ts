export function buildQuoteChecklist() {
  return [
    "Ask for a cash-only quote and a financed quote side by side.",
    "Confirm every equipment model number in writing.",
    "Make the dealer fee explicit.",
    "Ask for the production assumptions and shading assumptions.",
    "Ask whether the battery is for backup, savings, or both.",
    "Request a battery-less version for comparison.",
    "Compare the quote against at least two other installers.",
    "Do not sign until the numbers make sense in writing.",
  ];
}

export function buildInstallerQuestions() {
  return [
    "What is the exact cash price per watt?",
    "What is the dealer fee in dollars and percent?",
    "What production assumptions are you using?",
    "Is the battery justified for backup or economics?",
    "Can I see the same system without the battery?",
    "What is the warranty coverage for the inverter and panels?",
  ];
}

export function buildNextStepsAfterReview() {
  return [
    "Ask for a revised cash-only quote.",
    "Get two more bids for the same system size.",
    "Compare battery and non-battery versions separately.",
    "Check the warranty and equipment model numbers before signing.",
  ];
}